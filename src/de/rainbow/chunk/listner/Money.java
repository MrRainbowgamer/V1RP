package de.rainbow.chunk.listner;

public class Money {


    public static int money = 9999999;


    public static void setMoney(int money2) {
        money = money2;
    }

    public static void addMoney(int money2) {
        money = money + money2;
    }

    public static void delMoney(int money2) {
        money =  money - money2;
    }


}
